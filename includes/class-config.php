	/**
	 * Get the dropin file path for a given backend.
	 *
	 * @param string $backend Backend slug.
	 * @return string|false
	 */
	public static function get_object_cache_dropin_path( $backend ) {
		// Object cache dropins are in the optional add-on.
		$pro_path = defined( 'PRIME_CACHE_PRO_PATH' ) ? PRIME_CACHE_PRO_PATH : PRIME_CACHE_PATH;
		$dropins = array(
			'apcu'      => $pro_path . 'dropins/apcu-object-cache.php',
			'redis'     => $pro_path . 'dropins/redis-object-cache.php',
			'memcached' => $pro_path . 'dropins/memcached-object-cache.php',
		);

		return isset( $dropins[ $backend ] ) ? $dropins[ $backend ] : false;
	}

	/**
	 * Install or remove the object-cache.php dropin.
	 *
	 * @param string $backend Backend slug ('off' to remove).
	 * @return bool
	 */
	public static function setup_object_cache( $backend = 'off' ) {
		$file = WP_CONTENT_DIR . '/object-cache.php';

		// Remove if turning off.
		if ( 'off' === $backend ) {
			if ( file_exists( $file ) ) {
				$content = file_get_contents( $file ); // phpcs:ignore WordPress.WP.AlternativeFunctions.file_get_contents_file_get_contents
				if ( false === $content ) {
					return false; // Unreadable — refuse to delete an unidentified dropin.
				}
				if ( false !== strpos( $content, 'PRIME_CACHE_DROPIN_SIGNATURE' ) ) {
					// Surface unlink failure (permissions, opcache holding the
					// file) so callers don't render a misleading "Disabled"
					// notice while the dropin is still on disk and active.
					return (bool) @unlink( $file );
				}
				// External drop-in exists — cannot disable, report failure.
				return false;
			}
			return true;
		}

		$dropin_source = self::get_object_cache_dropin_path( $backend );
		if ( ! $dropin_source || ! is_readable( $dropin_source ) ) {
			return false;
		}

		// Refuse to overwrite another plugin's object-cache.php.
		if ( file_exists( $file ) ) {
			$existing = file_get_contents( $file ); // phpcs:ignore
			// Unreadable or not ours — do not overwrite.
			if ( false === $existing || false === strpos( $existing, 'PRIME_CACHE_DROPIN_SIGNATURE' ) ) {
				return false;
			}
		}

		$content = self::get_object_cache_content( $backend, $dropin_source );

		// Atomic write: temp file + rename.
		$tempfile = $file . '.tmp.' . getmypid();
		// phpcs:ignore WordPress.WP.AlternativeFunctions.file_system_operations_file_put_contents,PluginCheck.CodeAnalysis.WriteFile.PluginDirectoryWrite -- Writes the official WordPress object-cache.php drop-in into WP_CONTENT_DIR (sibling of the plugin folder), not into the plugin directory. Standard PHP file ops are required for the atomic temp+rename install pattern.
		if ( false === file_put_contents( $tempfile, $content ) ) {
			return false;
		}
		// phpcs:ignore WordPress.WP.AlternativeFunctions.rename_rename,PluginCheck.CodeAnalysis.WriteFile.PluginDirectoryWrite -- rename() completes the atomic install of wp-content/object-cache.php (WP drop-in), not a file inside the plugin directory.
		if ( ! rename( $tempfile, $file ) ) {
			@unlink( $tempfile );
			return false;
		}
		return true;
	}

	/**
	 * Generate the object-cache.php dropin content.
	 *
	 * This dropin is loaded by WordPress before plugins. It sets up a cache key
	 * salt for multisite isolation, then loads the selected backend implementation.
	 *
	 * @param string $backend       Backend slug (apcu, redis, memcached).
	 * @param string $dropin_source Absolute path to the backend dropin file.
	 * @return string Generated PHP code.
	 */
	private static function get_object_cache_content( $backend, $dropin_source ) {
		$escaped_path = addslashes( $dropin_source );
		$version      = PRIME_CACHE_VERSION;
		$label        = ucfirst( $backend );

		return "<?php
// PRIME_CACHE_DROPIN_SIGNATURE — do not remove this marker.
/**
 * Object Cache dropin — {$label} backend.
 * Generated by Prime Cache {$version}. Do not edit manually.
 */
defined( 'ABSPATH' ) || exit;

// Prime Cache manages its own cache files directly for performance; the
// WP_Filesystem API is not used on these cache paths. Disable the direct-file
// sniff for this module.
// phpcs:disable WordPress.WP.AlternativeFunctions
define( 'PRIME_CACHE_OBJECT_CACHE_DROPIN', true );

// Ensure cache keys are unique per WordPress install. DB_NAME alone collides
// when two installs share a database via different table_prefix; mixing in an
// ABSPATH+AUTH_SALT hash makes the salt install-unique without depending on
// runtime state (\$table_prefix is not set at object-cache.php load time).
if ( ! defined( 'WP_CACHE_KEY_SALT' ) ) {
	\$pc_salt_seed = ( defined( 'DB_NAME' ) ? DB_NAME : '' )
		. '|' . ABSPATH
		. '|' . ( defined( 'AUTH_SALT' ) ? AUTH_SALT : '' );
	define( 'WP_CACHE_KEY_SALT', ( defined( 'DB_NAME' ) ? DB_NAME : '' ) . ':' . substr( md5( \$pc_salt_seed ), 0, 8 ) . ':' );
	unset( \$pc_salt_seed );
}

// Load the backend implementation.
\$backend_file = '{$escaped_path}';
if ( is_readable( \$backend_file ) ) {
	require_once \$backend_file;
}
";
	}

	/**
	 * Get the currently active object cache backend.
	 *
	 * @return string Backend slug or 'off'.
	 */
	public static function get_active_object_cache() {
		$file = WP_CONTENT_DIR . '/object-cache.php';
		if ( ! file_exists( $file ) ) {
			return 'off';
		}

		$content = file_get_contents( $file ); // phpcs:ignore WordPress.WP.AlternativeFunctions.file_get_contents_file_get_contents
		// Unreadable: report 'external' so callers don't assume it's ours.
		if ( false === $content || false === strpos( $content, 'PRIME_CACHE_DROPIN_SIGNATURE' ) ) {
			return 'external';
		}

		// Determine which backend the dropin references, then verify the backend
		// file is actually readable. The dropin no-ops at runtime when the
		// backend file is missing (`if ( is_readable( $backend_file ) )`), so
		// reporting the slug alone would make the UI/CLI claim caching is on
		// while no caching is happening. Return 'broken' so callers can surface
		// the mismatch instead of silently lying.
		$backend = '';
		if ( false !== strpos( $content, 'apcu-object-cache.php' ) ) {
			$backend = 'apcu';
		} elseif ( false !== strpos( $content, 'redis-object-cache.php' ) ) {
			$backend = 'redis';
		} elseif ( false !== strpos( $content, 'memcached-object-cache.php' ) ) {
			$backend = 'memcached';
		}

		if ( '' === $backend ) {
			return 'off';
		}

		$backend_path = self::get_object_cache_dropin_path( $backend );
		if ( ! $backend_path || ! is_readable( $backend_path ) ) {
			return 'broken';
		}

		return $backend;
	}

	/**
	 * Find wp-config.php path.
	 *
	 * @return string|false
	 */
	private static function get_wp_config_path() {
		if ( file_exists( ABSPATH . 'wp-config.php' ) ) {
			return ABSPATH . 'wp-config.php';
		}

		if ( file_exists( dirname( ABSPATH ) . '/wp-config.php' ) ) {
			return dirname( ABSPATH ) . '/wp-config.php';
		}

		return false;
	}

	/**
	 * Verify that WP_CACHE is set to true in wp-config.php by reading the file.
	 *
	 * This is necessary because PHP constants loaded at the start of the request
	 * do not reflect changes made by set_wp_cache() during the same request.
	 *
	 * @return bool True if wp-config.php contains WP_CACHE set to true.
	 */
	public static function verify_wp_cache_enabled() {
		$config_path = self::get_wp_config_path();
		if ( ! $config_path ) {
			return false;
		}

		$content = file_get_contents( $config_path ); // phpcs:ignore WordPress.WP.AlternativeFunctions.file_get_contents_file_get_contents
		if ( false === $content ) {
			return false;
		}

		// Use PHP's tokenizer for accurate parsing — avoids false matches
		// inside strings, comments, or malformed code.
		$tokens = token_get_all( $content );
		$count  = count( $tokens );

		for ( $i = 0; $i < $count; $i++ ) {
			// Look for: define ( 'WP_CACHE' , <value> )
			if ( ! is_array( $tokens[ $i ] ) || T_STRING !== $tokens[ $i ][0] || 'define' !== strtolower( $tokens[ $i ][1] ) ) {
				continue;
			}

			// Scan forward for ( 'WP_CACHE' , <value> ).
			$j = $i + 1;
			// Skip whitespace.
			while ( $j < $count && is_array( $tokens[ $j ] ) && T_WHITESPACE === $tokens[ $j ][0] ) { $j++; }
			// Expect (
			if ( $j >= $count || '(' !== $tokens[ $j ] ) { continue; }
			$j++;
			while ( $j < $count && is_array( $tokens[ $j ] ) && T_WHITESPACE === $tokens[ $j ][0] ) { $j++; }
			// Expect string constant 'WP_CACHE' or "WP_CACHE".
			if ( $j >= $count || ! is_array( $tokens[ $j ] ) || T_CONSTANT_ENCAPSED_STRING !== $tokens[ $j ][0] ) { continue; }
			$const_name = trim( $tokens[ $j ][1], "\"'" );
			if ( 'WP_CACHE' !== $const_name ) { continue; }
			$j++;
			while ( $j < $count && is_array( $tokens[ $j ] ) && T_WHITESPACE === $tokens[ $j ][0] ) { $j++; }
			// Expect comma.
			if ( $j >= $count || ',' !== $tokens[ $j ] ) { continue; }
			$j++;
			while ( $j < $count && is_array( $tokens[ $j ] ) && T_WHITESPACE === $tokens[ $j ][0] ) { $j++; }
			// Read the value token.
			if ( $j >= $count ) { continue; }

			if ( is_array( $tokens[ $j ] ) ) {
				$val = strtolower( $tokens[ $j ][1] );
				// PHP uses first define() — return immediately.
				return in_array( $val, array( 'true', '1' ), true );
			}

			// Non-array token (e.g. a number literal).
			return '1' === $tokens[ $j ];
		}

		return false; // No WP_CACHE definition found.
	}
